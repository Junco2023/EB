private void buscar(){
    String [] f=new String [8];
    if(jrbBuscar.isSelected()==true){
    f[0]=(jtId.getText());
    f[1]=(jcbEspecie.getSelectedItem().toString());
    f[2]=jtNombre.getText();
    f[3]=jtVar.getText();
    f[4]=(jtMac.getText());
    f[5]=(jtStock.getText());
    f[6]=(jtPrecio.getText());
    f[7]=(jtPeso.getText());
    
    CD.filtrar(f);
    }
}


